package week1.day1;



public class IsPrimenumber {

	public static void main(String[] args) {
		
		int n=10;
		for(int i=2;i<n;i++)
		{
			if(n%i ==0)
			{
				System.out.println("It is not a Prime Number");
				return;
			}
		
		else
		{
		System.out.println("It is a Prime Number");
		return;
	
	}
		
}
}
}
	
